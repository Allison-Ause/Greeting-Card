
export function add(x, y) {
    return x + y;
}

export function subtract(x, y) {
    return x - y;
}

export function multiply(x, y) {
    return x * y;
}

export function convertToSeconds(minutes) {
    return minutes * 60;

}

export function areaOfTriangle(base, height) {
    return (base * height) / 2;
}